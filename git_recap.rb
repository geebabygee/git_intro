# GIT PROCESS
# 1. git init - start tracking my work, only once at beginning of project
# 2. git status - what has changed, what have I added/deleted or modified
# 3. git add <name of file> --> add this particular file/ git add . --> add all the files that you've been working on
# 4. git commit -m "make a message of what you've done"
# 5. git push origin master --> push my master (local default branch) to Github repo (origin)
